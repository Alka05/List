package list;

public class ListMarks_List {

	public static void main(String[] args) {
		int[] marks_list = { 70, 27, 56, 78, 45 };//list of marks with data type
		for (int i = 0; i < marks_list.length; i++) {//loop starting with 0 because our index start with 0 and less then list length 
			System.out.print(marks_list[i] + " ");//print whole list number


		}
	}
}
